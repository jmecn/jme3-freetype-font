package io.github.jmecn.font;

/**
 * represents a direction in the Unicode bidirectional algorithm.
 *
 * @author yanmaoyuan
 */
public enum Direction {
    LTR,// left to right
    RTL,// right to left
    TTB,// top to bottom
    TTB_RTL,// top to bottom, right to left
    TTB_LTR,// top to bottom, left to right
    ;
}
